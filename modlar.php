        <font color="red" size="2" ><h2 align="center">TERRORIST HUNT (PVE) </font><br>
    
	<p><font color="black" size="2"> Botlara karşı 5 oyuncunun oynadığı bir oyun modu. Saldıran ya da Savunan olabiliyoruz. Sistem aynı Multiplayer modunda olduğu gibi ama tek fark burada bot dalgalarına karşı ayakta kalmaya çalışıyoruz. Botların zorluk seviyesini seçebiliyoruz. İstersek başkarıyla, istersek de tek başımıza(lone wolf-yalnız kurt) ya da sadece arkadaşlarımızla birlikte oynayabiliyoruz.</font></p>
  <font color="red" size="2" ><h2 align="center">SITUATIONS </font><br>
  <p><font color="black" size="2">Situations, tek başımıza botlara karşı oynadığımız bir mod. Bu mod bizi oyuna hazırlıyor. Oyunun dinamiklerini, nasıl oynanıldığını öğretiyor. Situations modu 10 bölümden oluşuyor. Bölümleri normal, hard ya da realistic zorluk seçenekleriyle oynayabiliyoruz. Zorluğa göre kazandığımız xp ve renown miktarı değişiyor. Ayrıca her bölümün 3’er tane görevi var. Bu görevleri yerine getirirsek ekstradan 200’er renown kazanıyoruz.<font></p>
   
   <font color="red" size="2" ><h2 align="center">MULTIPLAYER (PVP) </font>
	   <p><font color="black" size="2" > Oyunda 3 farklı oyun modu özellikleri:</font><br>
	   <font color="red" size="2">• HOSTAGE:</font><font color="black" size="2">Saldırı timi bir binadan rehineyi bulup kurtarmalı,savunma timi ise barikat ve tuzaklar kurarak saldırı timinin rehineyi almasını engellemelidir.</font><br>
	    <font color="red" size="2">• SECURE AREA:</font><font color="black" size="2">Saldırı timi , bir biyolojik tehlike arz eden bombanın yerini tespit etmeli ve içinde ardı ardına 10 saniye kalacak şekilde sabitlemelidir.</font><br>
	   <font color="red" size="2">• BOMB:</font><font color="black" size="2">Saldırı timi iki bombadan birini bulup defuse kit ile bombayı silahsızlandırmaya çalışırlar veya savunucu timini yok ederek turu kazanırlar. </font><br>
	   <font color="red" size="2" ><h2 align="center">MULTIPLAYER RANKED </font>
	   <p><font color="black" size="2">
	   -5 kişi olarak oyuna başlanıyor oyun bizi hangi tarafta oynayacaksak oraya kendisi otomatik atıyor biz seçemiyoruz.( Saldırı yada Savunucu) Atama yapıldıktan sonra karakterler seçiliyor ve oyuna başlanıyor.<br>
	   
	   -20. seviyede açılıyor.<br>-Takım arkadaşlarımızı duvar arkasından göremiyoruz.<br>

       -Savunan iken yakınlarımıza bir el bombası atılmışsa, bunun uyarısını göremiyoruz.<br>

       -Ekipmanlara hasar verdiğimizde ya da yok ettiğimizde çıkan küçük skor bildirimlerini göremiyoruz.<br>

       -Bizi öldürenin nasıl öldürdüğünü gösteren kill-cam bulunmuyor.<br>

       -Savunanlar bombanın ya da rehinenin bulunacağı alanı oylama ile seçebiliyor. Saldıranlar ise oyuna başlayacağı alanı seçebiliyor.<br>

       -Rekabetçi modda maç kazandıkça ya da kaybettikçe rütbe atlıyoruz ya düşüyoruz. Bu rütbe sistemi ise denk oyuncuların birbiriyle eşleşmesine yarıyor.</font></P>
