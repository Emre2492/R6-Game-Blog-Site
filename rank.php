      <p><font  size="3" color="black"> Bir oyuncunun beceri sıralaması ilk önce 10 sıralı maçı başarıyla tamamladıktan sonra elde edilir.
	  Oyuncunun rank beceri sıralaması aşağıdaki maddelere dayanılarak değiştirilir.En kötü beceri sıralaması Bronz IV , en iyi beceri sıralaması Diamond olmaktadır.</font></p>
      <p><font  size="3"color="black">-Oyundaki oyuncuların ve takımların beceri seviyeleri.<br>
	  -Oyuncu maçı kazanmış olsun yada olmasın.</br>
	  -Skor ve öldürme / Ölüm oranı sıralamaya göre önem taşımaktadır.</br>
	  -Maçın kimin kazandığı.</br>
	  -Eğer bronz seviyelerden olan takımın altın seviyeye sahip bir takıma kaybetmesi takımın beceri seviyesini çok fazla derecede etkilemez ancak tam tersi bir durum söz konusu ise her iki takımında sırası büyük oranda değişecektir.</br>
	  -Maçtan erken çıkan bir oyuncunun takımı oyunu kazanırsa çıkan oyuncu galibiyet puanı kazanamaz tam tersi oyunu terkettiği için sıralamasında değişiklik olacaktır.</br>
	  -Karşı takıma oyun sonuçlanmadan teslim olunursa sıralama değişecektir.</br>
	  -Bağlantı sorunları olan oyuncular içinde sıralama bir kayıp olarak sayılır ve takımı maçı kazansa bile oyuncu oyundan düştüğü için sıralama puanı kazanamaz ve oyundan ayrılmış gibi sayıldığı için sıralama kaybeder.
